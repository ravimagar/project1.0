package com.company;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("addition of "+(a+b));   //  Addition
        System.out.println("subtraction of"+(a-b)); // subtraction
        System.out.println("multiple of " +(a*b));    // multiplication
        System.out.println("division of "+ (b/a));    // division
        System.out.println("div of "+ (b%a));       //reminder
        System.out.println("div of =" +(b/a));          // quotient



    }
}
