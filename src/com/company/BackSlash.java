package com.company;

public class BackSlash {
    public static void main(String[] args) {
        int b = 20;
        int a = 10;
        System.out.println("addition of "+(a+b));   //' single quote // "" double quote
        System.out.println("\nsubtraction of"+(a-b)); // new line
        System.out.println("\tmultiple of " +(a*b));    //tab
        System.out.println("\bdivision of "+ (b/a));    //blank space
        System.out.println("\r add of " +(a+b));    //carriage return
        System.out.println("minus of "+(a-b));



    }
}
