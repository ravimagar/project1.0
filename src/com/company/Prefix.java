package com.company;

public class Prefix {
    public static void main(String[] args) {
        int a =10;              //value increase by 1
        int b =++a;
        int f =--b;
        System.out.println(f);
        System.out.println(b);
        int c=20;               //first assign the increment
        int d=c++;
        int e=d--;
        System.out.println(e);
        System.out.println(d);
        System.out.println("add is"+ (a+d));

    }












}
